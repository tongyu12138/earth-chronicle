// 同一媒体位出现项目自有 AI 复原时，以已审核的 AI 记录替换旧来源，避免重复 ID。
const aiMediaIds = new Set(aiMediaCatalog.map((item) => item.id))
const mediaCatalog = curatedMediaCatalog
  .concat(syncedMediaCatalog)
  .filter((item) => !aiMediaIds.has(item.id))
  .concat(aiMediaCatalog)

const mediaMap = mediaCatalog.reduce((map, item) => {
  map[item.id] = Object.freeze(Object.assign({}, item))
  return map
}, {})

function missingMedia(id, fallback) {
  return Object.assign({
    id: id || '',
    ownerType: '',
    ownerId: '',
    thumbnailUrl: '',
    imageUrl: '',
    galleryUrls: [],
    alt: '图片资料待补充',
    caption: '',
    credit: '',
    author: '',
    license: '',
    sourceUrl: '',
    isReconstruction: false,
    mediaType: '',
    scientificAccuracyNote: '',
    focalPoint: '50% 50%',
    priority: 'P2',
    requiredForRelease: false,
    status: 'missing'
  }, fallback || {})
}

function getMediaById(id, fallback) {
  const record = mediaMap[id]
  if (!record) return missingMedia(id, fallback)
  return Object.assign({}, record, {
    thumbnailUrl: resolveMediaUrl(record.thumbnailUrl),
    imageUrl: resolveMediaUrl(record.imageUrl),
    galleryUrls: (record.galleryUrls || []).map(resolveMediaUrl).filter(Boolean)
  })
}

